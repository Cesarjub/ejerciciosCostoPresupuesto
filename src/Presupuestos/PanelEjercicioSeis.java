    public void setA()
    {
        int viajesMes = 0;
        
        viajesMes = (int) (Integer.parseInt(viajesSemanales.getText()) * 4.3);
        
        float cf = 0.0f;
        
        cf = (Float.parseFloat(costoSueldo.getText()) * viajesMes) + Float.parseFloat(costosFijos.getText());
        
        float mc = 0.0f;
        
        mc = cf + Float.parseFloat(utilidad.getText());
        
        totalMargen.setText(formatoImporte.format(mc));
        
        float mcu = 0.0f;
        
        mcu = Float.parseFloat(tarifaUnica.getText()) - Float.parseFloat(costoVaria.getText());
        
        int paj = 0;
        
        paj = (int) (mc / mcu);
        
        totalPasajeros.setText(formatoNumero.format(paj));
    }
    
